package main

import (
	"fmt"
)

func main() {
	// Размер таблицы (можно изменить на любое число)
	n := 10

	fmt.Printf("   Таблица умножения %d x %d\n\n", n, n)

	// Печатаем верхнюю шапку (номера колонок)
	fmt.Print("    ") // Отступ для первой колонки
	for i := 1; i <= n; i++ {
		fmt.Printf("%4d", i)
	}
	fmt.Println()

	// Рисуем декоративную линию
	fmt.Print("    ")
	for i := 1; i <= n; i++ {
		fmt.Print("----")
	}
	fmt.Println()

	// Вложенные циклы для построения строк
	for i := 1; i <= n; i++ {
		// Печатаем номер строки (левая колонка)
		fmt.Printf("%2d |", i)

		for j := 1; j <= n; j++ {
			// Вычисляем результат
			result := i * j

			// Используем %4d для фиксированной ширины колонки (4 символа)
			// Это гарантирует, что числа не "поедут"
			fmt.Printf("%4d", result)
		}
		// Переход на новую строку после завершения внутреннего цикла
		fmt.Println()
	}
}
